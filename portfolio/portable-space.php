								<h5 class="m-t-0">What I did</h5>
								<p>Whilst I was redesigning the Portable Space website with Fellowship Productions website I was tasked with improving the UX of the Masthead.</p>
								<p>During research we discovered that users wanted to easily find more information about the business whilst being able to find the products they were looking for in a more efficient fashion.</p>
								<p>The top-level business lines / categories was expanded upon in the Information Architecture development so I had to ensure there was room for 7 business lines. These were made primary tabs running along the bottom of the masthead. Sections and information about the business, blog, etc was moved into a secondary menu with drop-down menus revealing further content.</p>
								<p>Search was still a major function users relied on as a navigation tool. We used an expanding search field which revealed popular items at first but when a user started typing ‘Suggested items’ were used.</p>
								<p>The result was a simple, clean structure which improved the UX of the masthead significantly and is now regularly tested with users for optimum performance.</p>
