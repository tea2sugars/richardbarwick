								<p>My brief was to re-design the Portable Space website to accommodate the expansion of the clients business. I was to pay particular attention to the UX of the masthead as the navigation and search functionality was proving difficult for users to find the right product. I provided the User Experience Design (UX) then designed the User Interface (UI) for the masthead and the new site.</p>
