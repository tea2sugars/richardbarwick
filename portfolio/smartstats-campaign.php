								<h5 class="m-t-0">What we did</h5>
								<p>We faced a challenging weekly deadline, and with the client and the PR team based in different locations, communication was key to the success of this project. We started with short design phases where the structure of the ad’s artwork and overall design language was set in place, allowing more time for creating the different themes each week.</p>

<p>Each ad artwork focused on either a topical news or event happening that week, making each artwork unique in its approach in design but with naturally occurring branding. The entire collection of artworks features bespoke designed infographics, theme styles and illustrations, all embedded with the colours and tone of voice of Carphone Warehouse. We knew that the best way to maximise the reach of the piece was to focus on making them fun and informative, so we gave them a bright and playful appearance whilst also still making the content digestible.</p>

<p>We managed the final sign off stages of each artwork, working closely with the press production team ensuring that the artworks were delivered on time and correct to print.</p>

