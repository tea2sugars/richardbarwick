								<h5 class="m-t-0">My Brief</h5>
								<p>My brief was to work with Airbeem's client RVL who are a content creation company based in London. They asked me to help deliver Phase 1 of their website for their new music competition aimed at song writers, producers and musicians. The main KPI for the site was to convert both artists and fans to sign-up and register their interest in the competition.</p>
