							<article class="post-entry">

								<p class="lead">Creativitea - pronounced “Creativity” was setup in 2008 when the demand of the directors moonlighting work and freelance became too much.</p>

<p>Lloyd Bedford and Rik Barwick go way back! They met whilst Rik was studying his BA Hons at The Surrey Institute of Art & Design. Rik worked part time in a local video-rental shop and Lloyd worked across the street in a skate fashion retailer which eventually became Rik’s first professional gig; building their e-commerce store.</p>

<p>Rik and Lloyd became inseparable, hanging out in each others place of work and collaborating on designs and art pieces for the pure fun of it; often heading out after dark to tag and spray stencils around Epsom and Kingston to express themselves.</p>

<p>When Rik graduated in 2003 he went to work as a Junior Digital Designer at Carphone Warehouse where he continued to work in e-commerce and digital marketing, trying to change the culture within the studio to align the brand across digital and traditional design outputs the studio was in charge of.</p>

<p>Later that year, when an opportunity came up for a Junior Brand Designer, Lloyd took the role. Now, both working in London together, the dreams of setting up their own agency started; long commutes and late nights followed - and many cups of tea were drunk - whilst putting the creative world to rights and dreaming of doing something different.</p>

<p>In 2005, Rik wanted a new challenge and went agency-side and landed himself a job with Digital Marketing Agency WDMP of which his first client was, yes you guessed it Carphone Warehouse and Talk Talk. Rik had left, but not completely.</p>

<p>Whilst Lloyd was battling on at Carphone, Rik was taking on his own battles agency side and learning heaps about clients and working on multiple brands all at once. - <em>He loved it!</em></pM

<p>Rik hadn’t lost his entrepreneur-minded attitude and was regularly working on side-line projects having negotiated his contract to remove any clause saying he couldn’t. But then the phone rang which would change his life.</p>

<p>In 2007 Rik got a call from his old boss at CPW enquiring whether he wanted to lead a creative team and a dream client - Electronic Arts - on a freelance basis. He was currently looking about and had a role lined up as a Creative Director at a local agency but in the final hour chose Electronic Arts.</p>

<p>Whilst this was going on, Lloyd was making connections with the vast organisation at Carphone Warehouse, making himself irreplaceable across the business. So much so he started to have too much work on so it had to be outsourced. Rik was getting frustrated in paying fees to work freelance due to EA’s insistence in him being a Limited Company and Rik was picking up Print and Brand work which he was out-sourcing to Lloyd to do in the evenings.</p>

<p>In June 2008 pen was put to paper and Creativitea Limited was born. The agencies first clients were Electronic Arts, Talkmobile and Carphone Warehouse. Not a bad start to any agency journey to say the least!</p>

<p>The original name Lloyd chose for the agency was just 'Tea' but that was already taken so Rik thought of Creativitea. Which is a homage of the late nights having Skype calls &amp; drinking tea at 3am. It on reflection was probably a bad choice as name as we're constantly having to correct pronunciation and spelling but it does give us a talking point and is a nice nod to what got us here. There is also a marketing spiel about the name but it's so corny we'll have to tell you another day.</p>

<p>A few months later Lloyd left Carphone Warehouse to join Rik full time taking a heap of clients with him, becoming Talkmobile’s key creative team working on brand and above-the-line campaigns and the recommendations started to flow in.</p>

<p>Rik always believed in forgiveness and never burning a bridge. All the work we tend to get into the agency has come from recommendations. Some of which still date back to the Carphone Warehouse days where we continue to regularly enjoy working for. We’re proud of our recommendation method and love the fact we’re still working and advising on clients we worked on back when were junior designers.</p>

<p class="yellow lead">&ndash;</p>

<p>Since 2008 we’ve all grown up, got married and had kids and have certainly not tagged anything recently but we’re extremely proud of our roots and of having worked for many clients of all shapes and sizes. We’ve grown the business and shrunk the business but always maintained that we won’t force growth – It’ll be done organically when we need to. The agency business model over the past few years have changed. It’s more of a collaborative place now with people working all over the world to complete great projects and that’s how we work with our clients.</p>

<p>We’re looking forward to many more years in business working with old and new clients, building on the great relationships we’ve developed over the years. Hopefully you’ll be part of our future story!</p>
